#pragma once
#include "EditorDefine.h"
#include "Singleton.h"

namespace Editor
{
    // ********************************************************************************
    /// <summary>
    /// アニメイク用関数クラス
    /// </summary>
    // ********************************************************************************
    class CEditorFunc : public CSingleton<CEditorFunc>
    {
        friend class CSingleton<CEditorFunc>;
    public:
        // ********************************************************************************
        /// <summary>
        /// コンストラクタ
        /// </summary>
        // ********************************************************************************
        CEditorFunc() = default;

        // ********************************************************************************
        /// <summary>
        /// デストラクタ
        /// </summary>
        // ********************************************************************************
        ~CEditorFunc() = default;

        // ********************************************************************************
        /// <summary>
        /// ファイルの新規作成
        /// </summary>
        /// <param name="file">ファイルパス</param>
        /// <returns>TRUE 成功, それ以外 失敗</returns>
        // ********************************************************************************
        MofBool NewFile(LPCMofChar file);

        // ********************************************************************************
        /// <summary>
        /// ファイルの読み込み
        /// </summary>
        /// <returns>TRUE 成功, それ以外 失敗</returns>
        // ********************************************************************************
        MofBool OpenFile();

        // ********************************************************************************
        /// <summary>
        /// ファイルを閉じる
        /// </summary>
        /// <returns>TRUE 成功, それ以外 失敗</returns>
        // ********************************************************************************
        MofBool CloseFile();

        // ********************************************************************************
        /// <summary>
        /// ファイルの保存
        /// </summary>
        /// <returns>TRUE 成功, それ以外 失敗</returns>
        // ********************************************************************************
        MofBool SaveFile();

        // ********************************************************************************
        /// <summary>
        /// ファイルの保存
        /// </summary>
        /// <param name="file">ファイルパス</param>
        /// <returns>TRUE 成功, それ以外 失敗</returns>
        // ********************************************************************************
        MofBool SaveFile(LPCMofChar file);

        // ********************************************************************************
        /// <summary>
        /// 元に戻す
        /// </summary>
        /// <returns>TRUE 成功, それ以外 失敗</returns>
        // ********************************************************************************
        MofBool Undo();

        // ********************************************************************************
        /// <summary>
        /// やり直し
        /// </summary>
        /// <returns>TRUE 成功, それ以外 失敗</returns>
        // ********************************************************************************
        MofBool Redo();
    };
    // シングルトンアクセス
    #define theEditorFunc CEditorFunc::GetInstance()
}

