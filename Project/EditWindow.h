#pragma once
#include "EditorDefine.h"

namespace Editor
{

    // ********************************************************************************
    /// <summary>
    /// 編集ウィンドウ
    /// </summary>
    // ********************************************************************************
    class CEditWindow
    {
    private:

        TexturePtr m_RenderTarget;

        Vector2 m_Scroll;

        Vector2 m_WindowPos;

    public:

        // ********************************************************************************
        /// <summary>
        /// コンストラクタ
        /// </summary>
        // ********************************************************************************
        CEditWindow();
        
        // ********************************************************************************
        /// <summary>
        /// デストラクタ
        /// </summary>
        // ********************************************************************************
        ~CEditWindow();

        // ********************************************************************************
        /// <summary>
        /// 描画ターゲットの生成
        /// </summary>
        /// <param name="w">横幅</param>
        /// <param name="h">高さ</param>
        /// <returns>TRUE 成功, それ以外 失敗</returns>
        // ********************************************************************************
        MofBool Create(MofU32 w, MofU32 h);

        // ********************************************************************************
        /// <summary>
        /// 更新
        /// </summary>
        /// <returns>TRUE 成功, それ以外 失敗</returns>
        // ********************************************************************************
        MofBool Update();

        // ********************************************************************************
        /// <summary>
        /// 描画
        /// </summary>
        /// <returns>TRUE 成功, それ以外 失敗</returns>
        // ********************************************************************************
        MofBool Render();

        // ********************************************************************************
        /// <summary>
        /// 解放
        /// </summary>
        /// <returns>TRUE 成功, それ以外 失敗</returns>
        // ********************************************************************************
        MofBool Release();
    };
}

